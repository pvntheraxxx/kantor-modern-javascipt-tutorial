/* Возврат значения. Функция может вернуть результат, который будет
передан в вызвавший её код. Простейшим примером может служить
функция сложения двух чисел: */

function sum(a,b) {
    return a + b;
}

let result = sum(1,2);
console.log(result);

/* Вывод: настройки параметров функции определяются аргументами,
которые задаются в круглых скобках после названия функции */