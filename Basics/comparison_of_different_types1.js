/* Возможна следующая ситуация:
1. Два значения равны ;
2. Одно из них true как логическое значение, другое - false 
Например: */

let a = 0;
console.log(Boolean(a)); // false

let b = "0";
console.log(Boolean(b)); // true

console.log(a == b); // true

/* С точки зрения JavaScript, результат ожидаем. Равенство преобразует значения,
используя числовое преобразование, поэтому "0" становится 0. В то время
как явное преобразование с помощью Boolean использует другой набор
правил */