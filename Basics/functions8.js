/* Если мы хотим задать параметру text значение по умолчанию, 
мы должны указать его после =: */

function showMessage(from,text = "текст не добавлен") {
    console.log(from + ": " + text);
}

showMessage("Аня"); // Аня: текст не добавлен

/* Вывод: всегда имеется возможность изменить значения
параметров по умолчанию, чтобы вместо undefined то значение,
которое мы указываем. */