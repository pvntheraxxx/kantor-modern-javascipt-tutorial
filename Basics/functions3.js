/* Внешние переменные. У функции есть доступ к внешним
переменным, например: */

let userName = "Вася";

function showMessage() {
    let message = "Привет, " + userName;
    console.log(message);
}

showMessage(); // Привет, Вася

/* Вывод: Функция обладает полным доступом к внешним
переменным и может изменять их значение. */