/* Несмотря на своё название - логические операторы, 
данные операторы могут применяться к значениям любых
типов */

/* || (ИЛИ), логическое ИЛИ, логическое сложение */
console.log(true || true); // true
console.log(false || true); // true
console.log(true || false); // true
console.log(false || false); // false