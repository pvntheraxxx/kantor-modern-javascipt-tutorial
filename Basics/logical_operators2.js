/* Обычно оператор || используется в if для проверки
истинности любого из заданных условий. */
let hour = 10;

if (hour < 10 || hour > 18) {
    console.log("Офис закрыт.");
} else {
    console.log("Офис открыт");
}
/* Эта программа выведет сообщение "Офис закрыт."
если хотя бы одно из условий в выражении будет 
истинным. */
