/*
Приведение к числу, унарный +
Плюс + существует в двух формах: бинарной, которую мы
использовали в предыдущих примерах, и унарной. 
*/

// Не влияет на числа
let x = 1;
console.log(x); // 1

let y = -2;
console.log(+y); // -2

// Преобразует не числа в числа
console.log(+true); // 1
console.log(+""); // 0