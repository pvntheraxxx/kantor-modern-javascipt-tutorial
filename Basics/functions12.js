/* Альтернативные параметры по умолчанию. Иногда имеет смысл
присваивать значения по умолчанию для параметров не в объявлении
функции, а на более позднем этапе. Во время выполнения функции мы
можем проверить, передан ли параметр, сравнив его с undefined: */

function showMessage(text) {
    // .. 
    if (text === undefined) { // если параметр отсутствует
        text = "пустое сообщение";
    }
    console.log(text);
}

showMessage(); // пустое сообщение