/* Инкремент/декремент можно применить только к переменной. 
Попытка использовать его на значении, типа 5++, приведёт к ошибке.
Существует постфиксная и префиксная форма инкремента/декремента. */

let counter1 = 1;
let a1 = ++counter1; // (*)
console.log(a1); // 2 
// В строке (*) префиксная форма ++counter1 увеличивает
// counter и возвращает новое значение 2

let counter2 = 1;
let a2 = counter2++; // (*) меняем ++counter2 на counter2++
console.log(a2); // 1