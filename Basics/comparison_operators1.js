/* Все операторы сравнения возвращают значение логического типа:
true - означает "да", "верно", "истина", "1"
false - означает "нет", "неверно", "ложь", "0"
*/
console.log("2 > 1 equals : ", 2 > 1); // true (верно)
console.log("2 == 1 equals : ", 2 == 1); // false (неверно)
console.log("2 != 1 equals : ", 2 != 1); // true (верно)

let result = 5 > 4; // результат сравнения присваивается переменной result
console.log("Значение выражения 5 > 4 в переменной result равно : ",result);