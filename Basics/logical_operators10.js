/* Оператор && выполняет следующие действия: 
1. Вычисляет операнды слева направо.
2. Каждый операнд преобразует в логическое значение. Если
результат false, останавливается и возвращает исходное
значение этого операнда. 
3. Если все операнды были истинными, возвращается последний.
*/

let value1 = true;
let value2 = true;
let value3 = true;
result = value1 && value2 && value3;

console.log(result); // true