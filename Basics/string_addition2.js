/* 
Сложение и преобразование строк - это особенность
бинарного плюса +. Другие арифметические операторы
работают только с числами и всегда преобразуют
операнды в числа. Например, вычитание и деление.
*/ 
console.log(6 - '2'); // 4, '2' приводится к числу
console.log('6' / '2'); // 3, оба операнда приводятся к числам