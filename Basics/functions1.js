/* Функции могут быть вызваны по своему имени. Например: */

function showMessage() {
    console.log("Всем привет!");
}

showMessage(); // Всем привет
showMessage(); // Всем привет

/* showMessage() - это вызов функции showMessage */

/* Вывод: одно из главных предназначений функций: избавление
от дублирования кода. Если понадобится поменять сообщение или 
способ его вывода - достаточно изменить его в одном месте:
в функции, которая его выводит */