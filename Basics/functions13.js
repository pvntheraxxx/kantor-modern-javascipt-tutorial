/* Или мы можем использовать оператор || */

function showMessage(text) {
    // если значение text ложно или равняется undefined, тогда присвоить text значение "пусто"
    text = text || "пусто";
}

/* Вывод: Современные движки JavaScript поддерживаеют оператор
нулевого слияния ??. Его использование будет лучшей практикой, в случае,
если большинство ложных значений, таких как 0, следует 
расценивать как "нормальные". */