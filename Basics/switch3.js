/* Любое выражение может быть аргументом для switch/case */

let a = "1";
let b = 0;

switch (+a) {
    case b + 1:
        console.log("Выполнится, т.к. значением +a будет 1, что в точности равно b+1");
        break;
    default:
        console.log("Это не выпонитсяы");
}

/* Вывод: в этом примере выражение +a вычисляется в 1, что
совпадает с выражением b + 1 в case, и, следовательно, код 
в этом блоке будет выполнен. */