/* Многие операторы сравнения известны нам из математики */
a = 1; b = 2;
console.log("a = ", a);
console.log("b = ", b);

console.log("a > b equals : ",a > b);
console.log("a < b equals : ",a < b);
console.log("a >= b equals : ",a >= b);
console.log("a <= b equals : ",a <= b);
console.log("a == b equals : ",a == b);
console.log("a != b equals : ",a != b);
// != не равно

// Результат сравнения имеет логический тип
// Все операторы сравнения возвращают либо true, либо false