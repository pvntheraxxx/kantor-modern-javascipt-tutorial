/* Тип имеет значение. Нужно отметить, что проверка на 
равенство всегда строгая. Значения должны быть одного типа,
чтобы выполнялось равенство. */

let arg = "0";

switch (arg) {
    case "0":
    case "1":
        console.log("Один или ноль");
        break;
    case "2":
        console.log("Два");
        break;
    case 3: 
        console.log("Никогда не выполнится!");
        break;
    default:
        console.log("Неизвестное значение");
}