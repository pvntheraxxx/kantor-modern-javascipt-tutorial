/* Конструкция switch представляет собой более наглядный
способ сравнить выражение сразу с несколькими вариантами. */

switch (x) {
    case "value1": // if (x === 'value1') 
      break;
    case "value2": // if (x === 'value2') 
      break;
    default:
      break;
}

/* Переменная x проверяется на строгое равенство первому 
значению value1, затем второму value2 и так далее. 
Если соответствие установлено - switch начинает выполняться от 
соответствующей директивы case и далее, до ближайшего break 
(или до конца switch). Если ни один case не совпал - выполняется
(если есть) вариант default. */ 