const number = 2;

if (number === 0) {
    console.log("Вы ввели число 0");
}

if (number == 1) {
    console.log("Вы ввели число 1");
}

if (number === 2 || number === 3) {
    console.log("Вы ввели число 2, а может и 3");
}

