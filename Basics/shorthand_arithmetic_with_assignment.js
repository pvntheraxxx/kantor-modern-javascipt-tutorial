/* Часто нужно применить оператор к переменной и сохранить
результат в ней же, поэтому возникает необходимость в 
использовании сокращённой арифметики с присваиванием */

let q = 2;
q = q + 5;
q = q * 2;
// Эту запись можно укоротить при помощи совмещённых операторов

let n = 2;
n += 5; // теперь n = 7 (работает как n = n + 5)
n *= 2; // теперь n = 14 (работает как n = n * 2)
console.log(n); // 14