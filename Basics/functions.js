/* Объявление функции. Для создания функций мы можем использовать
объявление функции. Пример объявления функции: */

function showMessage() {
    console.log("Всем привет!");
}
showMessage();

/* Вначале идёт ключевое слово function, после него имя функции,
затем список параметров в круглых скобках через запятую 
(в вышеперечисленном примере он пустой) и, наконец, код функции,
также называемый "телом функции", внутри фигурных скобок.

function имя (параметры) {
    ...тело функции...
}

*/