/* Одной из наиболее частых числовых операций является 
увеличение или уменьшение на единицу. Для этого существуют
даже специальные операторы - инкременты и декременты. */

// Инкремент ++ увеличивает переменную на 1:
let counter1 = 2;
counter1++; // работает как counter = counter + 1, просто запись короче
console.log(counter1); // 3

// Декремент -- уменьшает переменную на 1:
let counter2 = 2;
counter2--; // работает как counter = counter - 1, просто запись короче
console.log(counter2); // 1
