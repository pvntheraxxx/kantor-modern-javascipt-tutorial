/* Мы можем выйти из цикла в любой момент с помощью специальной директивы break.
Например, следующий код подсчитывает сумму вводимых чисел до тех пор, пока 
посетитель их вводит, а затем - выдаёт: */
let sum = 0;

while (true) {
    let value = 0;
    if (!value) break; // (*)
    sum += value;
}

console.log("Сумма: " + sum);