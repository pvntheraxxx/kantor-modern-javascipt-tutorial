function showCount(count) {
    // если count равен undefined или null, показать "неизвестно"
    console.log(count ?? "неизвестно");
}

showCount(0); // 0
showCount(null); // неизвестно
showCount(); // неизвестно

/* Современные движки JavaScript поддерживают оператор 
нулевого слияния ??. Его использование будет лучшей практикой,
в случае, если большинство ложных значений, таких как 0,
следует расценивать как "нормальные". */