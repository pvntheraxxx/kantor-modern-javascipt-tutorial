function pow(x,n) {
    if (n < 0) {
        console.log("Отрицательные значения 'n' не поддерживаются");
        return;
    } 

    let result = 1;

    for (let i = 0; i < n; i++) {
        result *= x;
    }

    return console.log(result);
}

pow(2,5);

/* Второй вариант является более читабельным, потому что "особый случай" 
n < 0 обрабатывается на ранней стадии. После проверки можно переходить к
"основному" потоку кода без необходимости увеличения вложенности.*/