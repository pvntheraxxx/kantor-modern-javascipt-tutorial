/* Вертикальные отступы: пустые строки для разбивки кода 
на "логические блоки". Даже одну функцию можно разделить 
на логичесские блоки. В примере ниже разделены инициализация
переменных, основной цикл и возвращаемый результат: */

function pow(x,n) {
    let result = 1;
    // <--
    for (let i = 0; i < n; i++) {
        result *= x;
    }
    // <--
    return result;
}

