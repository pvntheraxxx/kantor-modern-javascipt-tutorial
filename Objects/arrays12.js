/* Массивы копируются по ссылке */

let fruits = [ "Банан" ];

let arr = fruits; 
// копируется по ссылке 
// (две переменные ссылаются на один и
// тот же массив)

console.log(arr === fruits); // true

arr.push("Груша"); 
// массив меняется по ссылке

console.log(fruits); 
// Банан, Груша - теперь два элемента