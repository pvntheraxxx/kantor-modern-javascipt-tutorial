/* Массивы не имеют ни Symbol.toPrimitive, ни
функционирующего valueOf, они реализуют только
преобразование toString, таким образом, здесь []
становится пустой строкой, [1] становится "1", а
[1,2] становится "1,2" */

console.log([] + 1); // "1"
console.log([1] + 1); // "11"
console.log([1,2] + 1); // "1,21"