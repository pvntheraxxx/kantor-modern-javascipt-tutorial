/* Сокращённая запись метода. Существует более короткий
синтаксис для методов в литерале объекта: */

// эти объекты делают одно и то же

user = {
    sayHi: function() { // function expression
        console.log("Привет!");
    }
};

// сокращённая запись выглядит лучше, не так ли?

user = {
    sayHi() { 
        console.log("Привет!");
    },
};

/* Почти во всех случаях сокращённый синтаксис 
предпочтителен. */