/* С другой стороны, если ключи не целочисленные, то они 
перебираются в порядке создания, например: */

let user = {
    name: "Sasha",
    surname: "Pushkin",
};
user.age = 27; // добавим ещё одно свойство

// не целочисленные свойства перечислены в порядке создания
for (let prop in user) {
    console.log(prop); // name, surname age
};
