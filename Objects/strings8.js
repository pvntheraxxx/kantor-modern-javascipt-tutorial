/* Доступ к символам. Получить символ, который занимает 
позицию pos, можно с помощью квадратных скобок: [pos].
Также можно использовать метод str.at(pos). Первый символ
занимает нулевую позицию: */

let str = `Hello`;

// получаем первый символ
console.log(str[0]); // H
console.log(str.at(0)); // H

// получаем последний символ
console.log(str[str.length - 1]); // o
console.log(str.at(-1)); 