/* Массивы */

// В массиве могут храниться
// элементы любого типа

// разные типы значений
let arr = 
[ 
    'Яблоко', 
    { name: 'Джон' }, 
    true, 
    function() { console.log('Привет!'); } 
];

// получить элемент с индексом 1 (объект)
// и затем показать его свойство
console.log(arr[1].name); // Джон

// получить элемент с индексом 3 (функция) 
// и выполнить её
arr[3](); // Привет!
