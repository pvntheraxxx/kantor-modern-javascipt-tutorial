/* Передача объекта в качестве аргумента */

let obj = {
    // toString обрабатывает все преобразования
    // в случае отсутствия других методов
    toString() {
        return "2";
    },
};

console.log(obj * 2); 
// 4, объект был преобразован к примитиву "2",
// затем умножение сделало его число