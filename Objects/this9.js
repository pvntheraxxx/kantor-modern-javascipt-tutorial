/* Вызов без объекта: this === undefined */
"use strict"; 

function sayHi() {
    console.log(this);
};

sayHi(); // undefined (только в строгом режиме)

/* В нестрогом режиме значением this будет глобальный объект. 
Это исторически сложившееся поведение this, которое 
исправляется использованием строгого режима ("use strict"). */