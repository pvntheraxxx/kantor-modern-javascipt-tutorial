/* Методы pop/push, shift/unshift */

// Методы push и unshift могут добавлять 
// сразу несколько элементов:

let fruits = ["Яблоко"];

fruits.push("Апельсин", "Груша");
fruits.unshift("Ананас", "Лимон");

// [ "Ананас", "Лимон", "Яблоко", "Апельсин", "Груша" ]
console.log(fruits);

// В конце массива с помощью push() добавлены
// "Апельсин" и "Груша"
// В начале массива с помощью unshift() добавлены
// "Ананас", "Лимон"
// Таким образом "Яблоко" оказалось в середине