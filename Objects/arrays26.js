/* Скопирован ли массив? */

let fruits = [ "Яблоки","Груша","Апельсин" ];

// добавляем новое значение в "копию"
let shoppingCart = fruits;
shoppingCart.push("Банан");

// что в fruits?
console.log(fruits.length);

/* Поскольку массив является объектом в JavaScript,
он имеет т.н. ссылочный тип, это означает, что мы
добавили элемент "Банан" и тем самым мы увеличим
количество элементов массива. Но копирование не произойдёт. */