/* Не сравнивайте массивы при помощи == */

console.log([] == []); // false
console.log([0] == [0]); // false

/* Технически эти массивы являются разными объектами.
Так что они не равны. Оператор == не выполняет поэлементное
сравнение. */

console.log(0 == []); // true
console.log('0' == []); // false