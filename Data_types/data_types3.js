/* Некоторые языки, такие как Java, позволяют явное 
создание "объектов-обёрток" для примитивов при помощи
такого синтаксиса как "new Number(1)" или "new Boolean(false)".
В JavaScript, это тоже возможно по историческим причинам,
но очень не рекомендуется. В некоторых местах последствия
могут быть катастрофическими. Например: */

console.log(typeof 0); // "число"
console.log(typeof new Number(0)); // "object"