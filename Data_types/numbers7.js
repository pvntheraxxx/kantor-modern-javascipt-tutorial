/* Если число слишком большое, оно переполнит 64-битное 
хранилище, JavaScript вернёт бесконечность: */

console.log(1e500); // Infinity

/* Наиболее часто встречающаяся ошибка при работе с 
числами в JavaScript - это потеря точности */
console.log(0.1 + 0.2 == 0.3); // false
console.log(0.1 + 0.2); // 0.30000000000000004